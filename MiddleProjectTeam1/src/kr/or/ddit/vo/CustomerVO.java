package kr.or.ddit.vo;

public class CustomerVO {
	private String cus_id    ;
	private String cus_pw    ;
	private String cus_gu    ;
	private String cus_del   ;
	private String cus_date  ;
	public String getCus_id() {
		return cus_id;
	}
	public void setCus_id(String cus_id) {
		this.cus_id = cus_id;
	}
	public String getCus_pw() {
		return cus_pw;
	}
	public void setCus_pw(String cus_pw) {
		this.cus_pw = cus_pw;
	}
	public String getCus_gu() {
		return cus_gu;
	}
	public void setCus_gu(String cus_gu) {
		this.cus_gu = cus_gu;
	}
	public String getCus_del() {
		return cus_del;
	}
	public void setCus_del(String cus_del) {
		this.cus_del = cus_del;
	}
	public String getCus_date() {
		return cus_date;
	}
	public void setCus_date(String cus_date) {
		this.cus_date = cus_date;
	}
	

}
